mood = input("Kokia jūsų nuotaika? ")
if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Go on a meet with friends")
elif mood == "excited":
    print("Take it under control.")
elif mood == "relaxed":
    print("Keep that chill, bro.")
else:
    print("I don't recognize this mood")